# واجهة برمجة التطبيقات للمنتجات
# Products API

هذا المجلد يحتوي على ملفات واجهة برمجة التطبيقات (API) الخاصة بالمنتجات.

## نقاط النهاية المتاحة
- `GET /api/products/` - الحصول على قائمة المنتجات
- `GET /api/products/{id}` - الحصول على منتج محدد
- `POST /api/products/` - إنشاء منتج جديد (يتطلب مصادقة المسؤول)
- `PUT /api/products/{id}` - تحديث منتج موجود (يتطلب مصادقة المسؤول)
- `DELETE /api/products/{id}` - حذف منتج (يتطلب مصادقة المسؤول)
- `GET /api/products/category/{category_id}` - الحصول على منتجات حسب التصنيف
- `GET /api/products/search` - البحث في المنتجات

## المعلمات المدعومة
- `limit` - عدد النتائج في الصفحة (الافتراضي: 10)
- `page` - رقم الصفحة (الافتراضي: 1)
- `sort` - حقل الفرز (الافتراضي: id)
- `order` - ترتيب الفرز (asc أو desc، الافتراضي: asc)
- `fields` - الحقول المطلوبة (مفصولة بفواصل)

## أمثلة الاستخدام
```
GET /api/products/?limit=20&page=2&sort=price&order=desc
GET /api/products/search?q=laptop&category=3
```

## تنسيق الاستجابة
```json
{
  "status": "success",
  "data": [...],
  "meta": {
    "total": 100,
    "page": 2,
    "limit": 20,
    "pages": 5
  }
}
```

## رموز الحالة
- `200` - نجاح
- `201` - تم إنشاء العنصر بنجاح
- `400` - طلب غير صالح
- `401` - غير مصرح
- `403` - محظور
- `404` - غير موجود
- `500` - خطأ في الخادم

## ملاحظة
يجب إرسال رمز المصادقة في رأس الطلب للعمليات التي تتطلب مصادقة:
```
Authorization: Bearer {token}
```
